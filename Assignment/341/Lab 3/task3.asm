.MODEL SMALL

.STACK 100H

.DATA

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE


MOV AX, 0
CMP AX, 0   
JE ZERO
JG POSITIVE
JL NEGATIVE

ZERO:
MOV BX, 0
JMP EXIT

POSITIVE:
MOV BX, 1
JMP EXIT

NEGATIVE:
MOV BX, -1
JMP EXIT

       
       
; EXIT DOS

EXIT:    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
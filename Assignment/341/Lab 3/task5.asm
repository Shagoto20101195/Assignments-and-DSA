.MODEL SMALL

.STACK 100H

.DATA

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


MOV AH, 1
INT 21H
CMP AL, 41H
JGE CONDITION
JMP EXIT

CONDITION:
CMP AL, 5AH
JLE PRINT
JMP EXIT

PRINT:
MOV AH, 2
MOV DL, AL
INT 21H


MOV AX, 1000   
       
       
; EXIT DOS

EXIT:    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
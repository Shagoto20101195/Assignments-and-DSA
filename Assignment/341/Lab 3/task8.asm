.MODEL SMALL

.STACK 100H

.DATA

MSG1 DB "VOWEL$"
MSG2 DB "CONSONANT$"

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

MOV AH, 1
INT 21H

CMP AL, 41H
JE VOWEL
CMP AL, 45H
JE VOWEL
CMP AL, 49H
JE VOWEL
CMP AL, 4FH
JE VOWEL
CMP AL, 55H
JE VOWEL
JMP CONSONANT       

VOWEL:
MOV AH, 2
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

LEA DX, MSG1
MOV AH, 9
INT 21H
JMP EXIT

CONSONANT:
MOV AH, 2
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

LEA DX, MSG2
MOV AH, 9
INT 21H
JMP EXIT
       
; EXIT DOS

EXIT:    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
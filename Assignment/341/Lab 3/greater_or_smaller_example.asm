.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

LEA DX, MSG1
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
MOV BL, AL

; NEWLINE
MOV AH, 2
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

LEA DX, MSG2
MOV AH, 9
INT 21H

MOV AH, 1
INT 21H
MOV CL, AL

; NEWLINE
MOV AH, 2

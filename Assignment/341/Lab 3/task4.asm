.MODEL SMALL

.STACK 100H

.DATA

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

MOV AH, 1
INT 21H

CMP AL, 31H
JE ONE_OR_THREE
CMP AL, 33H
JE ONE_OR_THREE

CMP AL, 32H
JE TWO_OR_FOUR
CMP AL, 34H
JE TWO_OR_FOUR

ONE_OR_THREE:
MOV AH, 2
MOV DL, 6FH
INT 21H
JMP EXIT

TWO_OR_FOUR:
MOV AH, 2
MOV DL, 65H
INT 21H
JMP EXIT   
       
       
; EXIT DOS

EXIT:    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
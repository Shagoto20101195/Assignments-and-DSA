.MODEL SMALL

.STACK 100H

.DATA

MSG1 DB "ENTER THE VALUE OF N: $"

MSG2 DB "OUTPUT: $"
;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

; INPUT
LEA DX, MSG1
MOV AH, 9H
INT 21H
MOV AH, 1H
INT 21H
SUB AL, 30H
MOV CL, AL

; NEWLINE
MOV AH, 2H
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

; CALCULATION
MOV AL, 0H
MOV BL, 1H
WHILE:
CMP BL, CL
JNLE EXIT
ADD AL, BL
ADD BL, 2H
JMP WHILE    

EXIT:
MOV CL, AL
LEA DX, MSG2
MOV AH, 9H
INT 21H
MOV AH, 2H
MOV DL, CL
ADD DL, 30H
INT 21H          
       
; EXIT DOS
    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
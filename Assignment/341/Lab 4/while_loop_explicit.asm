.MODEL SMALL

.STACK 100H

.DATA

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE


MOV AH, 2
MOV DL, 35H

WHILE:
CMP DL, 35H
JE EXIT
INT 21H

; NEWLINE
MOV BL, DL
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

MOV DL, BL

INC DL
JMP WHILE   
       
       
; EXIT DOS

EXIT:    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
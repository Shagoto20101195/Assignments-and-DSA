.MODEL SMALL

.STACK 100H

.DATA


ARR DB 2,1,3,5,4
;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

; FINDING LOWEST NUMBER

MOV CX, 4
MOV SI, 0
MOV DL, ARR[SI]
ADD SI, 1H

WHILE:
CMP SI, 5H
JE OUTPUT
CMP DL, ARR[SI]
JGE IFTRUE
ADD SI, 1H
LOOP WHILE

IFTRUE:
MOV DL, ARR[SI]
ADD SI, 1
JMP WHILE 
       
OUTPUT:
MOV AH, 2H
ADD DL, 30H
INT 21H       
; EXIT DOS
    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN
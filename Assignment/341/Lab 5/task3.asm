.MODEL SMALL

.STACK 100H

.DATA

ARR DB 7 DUP(?)

;VARIABLES

.CODE
MAIN PROC    
    
; INITIALIZE DS
    
MOV AX, @DATA
MOV DS, AX


; ENTER YOUR CODE

; INPUT
MOV CX, 7H
MOV AH, 1H
MOV SI, 0H
WHILE1:
INT 21H
MOV ARR[SI], AL
ADD SI, 1H
LOOP WHILE1

; NEWLINE
MOV AH, 2
MOV DL, 0DH
INT 21H
MOV DL, 0AH
INT 21H

; OUTPUT
MOV CX, 7H
MOV AH, 2H
MOV SI, 0H
WHILE2:
MOV DL, ARR[SI]
INT 21H
ADD SI, 1H
LOOP WHILE2
       
       
; EXIT DOS

    
MOV AX, 4C00H
INT 21H   
    

    
MAIN ENDP
END MAIN